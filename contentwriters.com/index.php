  <script type="text/javascript">
    function resizeHeight(objs) {
      let maxHeight = 0;
      $(objs).height('auto');

      $(objs).each(function(){
        if ($(this).height() > maxHeight) {
          maxHeight = $(this).outerHeight();
        }
      });
      $(objs).height(maxHeight);
    }

    (function ($, window, document) {
      let initCarousel = function() {
        let sliderSelector = $('.carousel');
        let cardsSelector = $('.carousel .card');
        let pageWidth = $(window).width();

        $(sliderSelector).multislider({interval: 5000, duration:1000});

        resizeHeight(cardsSelector);

        if (pageWidth >= 768) {
          $(cardsSelector).on('mouseenter mouseleave', function() {
            $(this).toggleClass('active');
          });
        } else {
          $(cardsSelector).first().addClass('active');
          $(sliderSelector).on('ms.after.animate', function(){
            $(cardsSelector).addClass('active');
          }).on('ms.before.animate', function(){
            $(cardsSelector).removeClass('active');
          });
        }
      };

      $(window).resize(function() {
        resizeHeight($('.carousel .card'));
      })

      $(document).ready(function () {
        /* Carousel Slider JS
         =============================================================== */
        initCarousel();
      });
    }(jQuery, window, document));
